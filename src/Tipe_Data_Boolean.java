public class Tipe_Data_Boolean {
    public static void main(String[] args) {
        // Tipe Data Boolean.
        // Tipe data boolean adalah tipe data yang memiliki 2 nilai, yaitu benar dan salah.
        // Nilai benar di representasikan dengan kata kunci true, sedangkan nilai salah direpresentasikan dengan kata kunci false.
        // Default nilai untuk boolean adalah false.

        //contoh penggunaan tipe data boolean.
        boolean benar = true;
        boolean salah = false;

        System.out.println(benar);
        System.out.println(salah);

    }
}
