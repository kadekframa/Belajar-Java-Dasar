public class Tipe_Data_Character {
    public static void main(String[] args) {
        // Tipe data Character Java.
        // Data character di Java direpresentasikan oleh tipe char.
        // Untuk membuat data char di Java, kita bisa menggunakan tanda '' (petik satu).

        //contoh penggunaan tipe data char.
        char k = 'K';
        char a = 'A';
        char d = 'D';
        char e = 'E';
        char k2 = 'K';

        System.out.println(k);
        System.out.println(a);
        System.out.println(d);
        System.out.println(e);
        System.out.println(k2);

    }
}