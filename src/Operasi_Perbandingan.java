public class Operasi_Perbandingan {
    public static void main(String[] args) {
        // Operasi Perbandingan di Java (>, <, >=, <=, ==, !=).
        // Operasi perbandingan adalah operasi untuk membandingankan dua buah data.
        // Operasi perbandingan adalah operasi yang menghasilkan nilai boolean (benar atau salah).
        // Jika hasil operasinya adalah benar, maka nilainya adalah true, jika hasil operasinya adalah salah, maka nilainya adalah false.

        // Contoh penggunaan operasi perbandingan di Java.
        int value1 = 100;
        int value2 = 94;

        System.out.println(value1 < value2);
        System.out.println(value1 > value2);
        System.out.println(value1 <= value2);
        System.out.println(value1 >= value2);
        System.out.println(value1 == value2);
        System.out.println(value1 != value2);

    }
}
