public class Tipe_Data_Number {
    public static void main(String[] args) {
        // Tipe data number dalam Java secara garis besar itu dibagi menjadi 2, yaitu:
        // - integer number (bilangan bulat)
        // - floating point number (bilangan pecahan atau desimal).

        // Initialisasi variabel.
        // variable integer number (bilangan bulat).
        byte iniByte = 100;
        short iniShort = 1000;
        int iniInt = 1000000;
        long iniLong = 1000000000;
        long iniLong2 = 1000000000L;

        //variable floating point number (bilangan pecahan atau desimal).
        float iniFloat = 10.10F;
        double iniDouble = 10.10;

        // print variabel.
        System.out.println("Ini adalah bilangan dengan tipe data byte :" + iniByte);
        System.out.println("Ini adalah bilangan dengan tipe data short :" + iniShort);
        System.out.println("Ini adalah bilangan dengan tipe data Integer :" + iniInt);
        System.out.println("Ini adalah bilangan dengan tipe data long :" + iniLong);
        System.out.println("Ini adalah bilangan dengan tipe data long :" + iniLong2);
        System.out.println("Ini adalah bilangan dengan tipe data float :" + iniFloat);
        System.out.println("Ini adalah bilangan dengan tipe data double :" + iniDouble);
        System.out.println("Tetap Semangat Berlatih Java Programming Language !!!");
    }
}